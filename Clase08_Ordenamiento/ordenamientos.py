def ordenSeleccion(lista):
    cant = 0
    for i in range(len(lista) -1):
        imin = i
        for j in range(i + 1, len(lista)):
            #print("i:", i, "j:", j)
            cant += 1
            if (lista[j] < lista[imin]):
                imin = j
        aux = lista[i]
        lista[i] = lista[imin]
        lista[imin] = aux
    print(cant)


def ordenBurbuja(lista):
    intercambio = True
    cant = 0
    while intercambio:
        intercambio = False
        for i in range(len(lista) - 1):
            cant += 1
            if (lista[i] > lista[i + 1]):
                lista[i], lista[i+1] = lista[i+1], lista[i]
                intercambio = True
    print(cant)


